# Created an empty set.
s = set()

# Added elements to the set.
s.add(1)
s.add(2)
s.add(3)
s.add(4)
s.add(5)

# Removed the numbers "2" and "5" from set.
s.remove(2) and s.remove(5)

# Printed set.
print(s)


# Shows the total elements within the "S" set.
print(f"There is a total of {len(s)} elements within the set!")